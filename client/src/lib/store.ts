import { create } from "zustand";
import { persist } from "zustand/middleware";

interface User {
  id: string;
  email: string;
  name: string;
  tone: string;
  timezone: string;
  defaultSignature?: string;
  aiEnabled: boolean;
  autoGenerateDrafts: boolean;
  enablePersonal: boolean;
  personalAiEnabled: boolean;
  allowImageStorage: boolean;
  hasCompletedOnboarding: boolean;
  isAuthenticated: boolean;
}

interface Workspace {
  id: string;
  name: string;
  role: string;
}

interface AppState {
  user: User;
  currentWorkspaceId: string | null;
  workspaces: Workspace[];
  setUser: (user: User) => void;
  login: () => void;
  logout: () => void;
  updateUser: (updates: Partial<User>) => void;
  setCurrentWorkspace: (workspaceId: string | null) => void;
  setWorkspaces: (workspaces: Workspace[]) => void;
}

export const useStore = create<AppState>()(
  persist(
    (set, get) => ({
      user: {
        id: "",
        email: "",
        name: "",
        tone: "direct",
        timezone: "UTC",
        aiEnabled: true,
        autoGenerateDrafts: true,
        enablePersonal: false,
        personalAiEnabled: true,
        allowImageStorage: false,
        hasCompletedOnboarding: false,
        isAuthenticated: false,
      },

      currentWorkspaceId: null,
      workspaces: [],

      setUser: (user) => set({ user: { ...user, isAuthenticated: true } }),

      login: () => set((state) => ({ 
        user: { ...state.user, isAuthenticated: true } 
      })),

      logout: () => set({
        user: {
          id: "",
          email: "",
          name: "",
          tone: "direct",
          timezone: "UTC",
          aiEnabled: true,
          autoGenerateDrafts: true,
          enablePersonal: false,
          personalAiEnabled: true,
          allowImageStorage: false,
          hasCompletedOnboarding: false,
          isAuthenticated: false,
        },
        currentWorkspaceId: null,
        workspaces: [],
      }),

      updateUser: (updates) => set((state) => ({
        user: { ...state.user, ...updates }
      })),

      setCurrentWorkspace: (workspaceId) => set({ currentWorkspaceId: workspaceId }),

      setWorkspaces: (workspaces) => set({ workspaces }),
    }),
    {
      name: "action-minutes-storage",
    }
  )
);
